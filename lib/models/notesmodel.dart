class Note {
  final String title;
  final String content;
  final String text;
  final String createdAt;
  final String modifiedAt;
  final String category;

  Note({
    required this.title,
    required this.content,
    required this.text,
    required this.createdAt,
    required this.modifiedAt,
    required this.category,
  });
}
